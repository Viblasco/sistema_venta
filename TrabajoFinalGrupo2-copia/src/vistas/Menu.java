    private void btnModificarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarClientesActionPerformed

        if ("".equalsIgnoreCase(idClienteC.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione fila a modificar");

        } else {
            if (!"".equalsIgnoreCase(nombreC.getText()) || !"".equalsIgnoreCase(apellidoC.getText())
                    || !"".equalsIgnoreCase(direcC.getText()) || !"".equalsIgnoreCase(telC.getText())) {
                c1.setNombre(nombreC.getText());
                c1.setApellido(apellidoC.getText());
                c1.setDomicilio(direcC.getText());
                c1.setTelefono(telC.getText());
                c1.setIdCliente(Integer.parseInt(idClienteC.getText()));
                clienteD.modificarCliente(c1);

                limpiarTabla();
                limpiarCliente();
                listarClientes();

            } else {
                JOptionPane.showMessageDialog(null, "Algunos de los campos estan vacios");
            }
        }
    }//GEN-LAST:event_btnModificarClientesActionPerformed

