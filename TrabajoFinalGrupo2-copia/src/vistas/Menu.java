    private void btnEiminarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEiminarClientesActionPerformed
        if (!"".equalsIgnoreCase(idClienteC.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "¿Seguro desea eliminar?");
            if (pregunta == 0) {

                int id = Integer.parseInt(idClienteC.getText());
                clienteD.eliminarCliente(id);

                limpiarTabla();
                limpiarCliente();
                listarClientes();

            }
        }

    }//GEN-LAST:event_btnEiminarClientesActionPerformed

