    /* Listar a los clientes*/
    public void listarClientes() {
        ArrayList<Cliente> listaC1 = clienteD.listarClientes();
        modelo = (DefaultTableModel) jTablaClientes.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < listaC1.size(); i++) {
            ob[0] = listaC1.get(i).getIdCliente();
            ob[1] = listaC1.get(i).getNombre();
            ob[2] = listaC1.get(i).getApellido();
            ob[3] = listaC1.get(i).getDomicilio();
            ob[4] = listaC1.get(i).getTelefono();
            modelo.addRow(ob);

        }
        jTablaClientes.setModel(modelo);
    }
